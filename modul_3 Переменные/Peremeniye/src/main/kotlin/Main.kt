fun main(args: Array<String>) {
    val milkPrice = 52
    val milkAmount = 2
    val milk = milkPrice * milkAmount;

    val tomatoPrice = 220
    val tomatoAmount = 0.5
    val tomato = tomatoPrice * tomatoAmount;

    val cucumberPrice = 114
    val cucumberAmount = 0.4
    val cucumber = cucumberAmount * cucumberPrice;

    val waterPrice = 40

    val resultPrice = milk + tomato + cucumber + waterPrice

    println(resultPrice)
}