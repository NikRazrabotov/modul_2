    <h5 fr-original-style="" data-darkreader-inline-color="" style="font-size: 1.25rem; margin-top: 0px; margin-bottom: 0; color: black; line-height: 1.2; font-weight: 500">
      Конвенциональные правила названий переменных
    </h5>
    <p fr-original-style="" data-darkreader-inline-color="" style="margin-top: 0px; margin-bottom: 12px; color: black; font-size: 16px; line-height: 22px">
      Конвенция имён позволяет разработчикам читать чужой код. Более того, качественно написанный код можно понять, даже не зная этот язык программирования.
    </p>
    <ol fr-original-style="" style="margin-top: 0px; margin-bottom: 0; font-size: 16px; line-height: 22px">
      <li fr-original-style="">
        Не нужно начинать название переменной со знака доллара или подчёркивания. Это не&#xa0;запрещено, но от такой практики отказались.
      </li>
      <li fr-original-style="">
        Если название состоит более чем из одного слова, используют camelCase («спину верблюда»). Первое слово пишут с маленькой буквы, а все остальные — с большой: <strong fr-original-style="" style="font-weight: 700"><span style="font-weight: 700;">userAge,</span></strong>&#xa0;<strong fr-original-style="" style="font-weight: 700"><span style="font-weight: 700;">availableStockAmount.</span></strong>
      </li>
      <li fr-original-style="">
        Для констант (неизменяемых переменных) можно использовать верхний регистр, а разные слова — разделять подчёркиванием:<br fr-original-style=""/><span fr-original-class="fr-class-code" fr-original-style="" data-darkreader-inline-border-top="" data-darkreader-inline-border-right="" data-darkreader-inline-border-bottom="" data-darkreader-inline-border-left="" data-darkreader-inline-bgimage="" style="border-top-color: rgb(204, 204, 204); border-right-color: rgb(204, 204, 204); border-bottom-color: rgb(204, 204, 204); border-left-color: rgb(204, 204, 204); background-position: 0% 0%; background-repeat: repeat; background-attachment: scroll; background-image: none; padding-top: 10px; padding-right: 10px; padding-bottom: 10px; padding-left: 10px; font-family: Courier New, Courier, monospace;">val EARTH_GRAVITY_ACCELERATION = 9.81</span><br fr-original-style=""/>Это единственное разрешённое конвенцией применение знака подчёркивания в названии переменной.
      </li>
      <li fr-original-style="">
        Используем осмысленные названия переменных, при чтении которых будет понятно, что они должны хранить.<br fr-original-style=""/>Удачные названия: itemLength (длина предмета), userName (имя пользователя), cashbackAmount (количество кешбэка).<br fr-original-style=""/>Неудачные названия: a, c, qwhbav, asdjhDvhjv, amount.<br fr-original-style=""/>В последнем случае использовано просто слово «количество» (amount), которое не привязано к конкретному объекту, поэтому непонятно, количество чего именно хранит эта переменная. Есть небольшие исключения по названию переменных одной буквой, но о них вы узнаете позже.
      </li>
    </ol>
