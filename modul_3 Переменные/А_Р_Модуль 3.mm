      </li>
      <li fr-original-style="">
        Ограничения на длину переменной нет, но стоит учитывать здравый смысл.
      </li>
      <li fr-original-style="">
        Переменная должна начинаться с буквы, знака «доллар» ($) или подчёркивания (_).
      </li>
      <li fr-original-style="">
        Пробелы в названиях недопустимы.
      </li>
      <li fr-original-style="">
        Следующие после первого символы могут быть буквами, цифрами, знаком доллара или подчёркиванием.
      </li>
      <li fr-original-style="">
        Название переменной не должно быть ключевым или зарезервированным словом языка. Ключевые слова — специальные слова-операторы, с помощью которых создаются новые команды. Вы уже познакомились с несколькими: val, var, Int. Полный список ключевых и зарезервированных слов — <a fr-original-style="" href="https://kotlinlang.ru/docs/keyword-reference.html#:~:text=%D0%A1%D0%BB%D0%B5%D0%B4%D1%83%D1%8E%D1%89%D0%B8%D0%B5%20%D1%81%D0%BB%D0%BE%D0%B2%D0%B0%20%D0%B4%D0%B5%D0%B9%D1%81%D1%82%D0%B2%D1%83%D1%8E%D1%82%20%D0%BA%D0%B0%D0%BA%20%D0%BA%D0%BB%D1%8E%D1%87%D0%B5%D0%B2%D1%8B%D0%B5%20%D0%B2%20%D1%81%D0%BF%D0%B8%D1%81%D0%BA%D0%B0%D1%85%20%D0%BC%D0%BE%D0%B4%D0%B8%D1%84%D0%B8%D0%BA%D0%B0%D1%82%D0%BE%D1%80%D0%BE%D0%B2%20%D0%BE%D0%B1%D1%8A%D1%8F%D0%B2%D0%BB%D0%B5%D0%BD%D0%B8%D0%B9%20%D0%B8,annotation%20%D0%BE%D0%B1%D1%8A%D1%8F%D0%B2%D0%BB%D1%8F%D0%B5%D1%82%20%D0%BA%D0%BB%D0%B0%D1%81%D1%81%20%D0%B0%D0%BD%D0%BD%D0%BE%D1%82%D0%B0%D1%86%D0%B8%D0%B8" rel="noopener noreferrer" target="_blank" style="text-decoration: none"><span style="text-decoration: none;">в справочнике</span></a>. Таких слов не так много, но учить их не нужно: IDE и компилятор подскажут, название будет недоступным.
      </li>
